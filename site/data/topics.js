// flatten helper used by topics page & flashcards
window.LEXORA_TOPICS_FLAT = (() => {
  const out = [];
  for (const lang of ['en', 'fr']) {
    for (const lvl of ['a1','a2','b1','b2','c1','c2']) {
      const bundle = window.LEXORA_TOPICS[lang][lvl];
      for (const cat of ['lexical','grammar','philology']) {
        const arr = bundle && bundle[cat];
        if (!arr) continue;
        arr.forEach((t) => out.push({ ...t, lang, level: lvl, category: cat }));
      }
    }
  }
  return out;
})();
